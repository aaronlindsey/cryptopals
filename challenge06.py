def edit_distance(a: bytes, b: bytes) -> int:
    dist = 0
    for i, j in zip(a, b):
        diff = i ^ j
        for _ in range(8):
            dist += diff & 1
            diff >>= 1
    return dist


def rank_key_sizes(data: bytes, max_key_size: int) -> List[int]:
    scores = {}
    scored_blocks = len(data) // max_key_size - 1

    for key_size in range(2, max_key_size+1):
        scores[key_size] = 0
        for i in range(0, scored_blocks*key_size, key_size):
            a = data[i:i+key_size]
            b = data[i+key_size:i+key_size*2]
            scores[key_size] += edit_distance(a, b) / key_size
        scores[key_size] /= scored_blocks

    return [key_size for key_size, _ in sorted(scores.items(), key=lambda x: x[1])]


def transpose(blocks):
    for i in range(len(blocks[0])):
        row = []
        for block in blocks:
            if i < len(block):
                row.append(block[i])
        yield row


def crack_repeating_key_xor(data: bytes) -> CrackResult:
    max_key_size = min(50, len(data) // 5)
    top_key_sizes = rank_key_sizes(data, max_key_size)[:5]

    result = CrackResult()
    for key_size in top_key_sizes:
        blocks = [list(data[i:i+key_size])
                  for i in range(0, len(data), key_size)]

        key = bytearray()
        transposed_plaintext_blocks = []
        for transposed_block in transpose(blocks):
            cracked_block = crack_single_char_xor(bytes(transposed_block))
            key.append(cracked_block.key[0])
            transposed_plaintext_blocks.append(list(cracked_block.plaintext))

        plaintext = bytearray()
        for plaintext_block in transpose(transposed_plaintext_blocks):
            for b in plaintext_block:
                plaintext.append(b)

        score = score_plaintext(plaintext)
        if score < result.score:
            result = CrackResult(score=score, key=bytes(
                key), plaintext=bytes(plaintext), ciphertext=data)

    return result


